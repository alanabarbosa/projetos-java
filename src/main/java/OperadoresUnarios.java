public class OperadoresUnarios {
     public static void main(String args[]) {
         int number = 5;
         
         number ++;
         System.out.println("Icremento: " + number);
         
         number --;
         System.out.println("Decremento: " + number);
         
         boolean verdadeiro = true;
         
         System.out.println("Inverteu " + !verdadeiro);
        
    }   

}
